type CharPalette = {
  skin: string;
  shirt: string;
  pants: string;
  hair: string;
  shoes: string;
};

// Template keys for character pixel data
const H = "hair";
const K = "skin";
const S = "shirt";
const P = "pants";
const O = "shoes";
const E = "#FFFFFF"; // eyes

type TemplateCell =
  | typeof H
  | typeof K
  | typeof S
  | typeof P
  | typeof O
  | typeof E
  | typeof _;

function resolveTemplate(
  template: TemplateCell[][],
  palette: CharPalette,
): SpriteData {
  return template.map((row) =>
    row.map((cell) => {
      if (cell === _) return "";
      if (cell === E) return E;
      if (cell === H) return palette.hair;
      if (cell === K) return palette.skin;
      if (cell === S) return palette.shirt;
      if (cell === P) return palette.pants;
      if (cell === O) return palette.shoes;
      return cell;
    }),
  );
}

function flipHorizontal(template: TemplateCell[][]): TemplateCell[][] {
  return template.map((row) => [...row].reverse());
}

