// ── Sprite resolution + caching ───────────────────────────────────

// Direction constants (match engine.ts values)
const DIR_DOWN = 0;
const DIR_LEFT = 1;
const DIR_RIGHT = 2;
const DIR_UP = 3;

const spriteCache = new Map<string, CharacterSprites>();

export function getCharacterSprites(paletteIndex: number): CharacterSprites {
  const cacheKey = `${paletteIndex}`;
  const cached = spriteCache.get(cacheKey);
  if (cached) return cached;

  const pal = CHARACTER_PALETTES[
    paletteIndex % CHARACTER_PALETTES.length
  ] as CharPalette;
  const r = (t: TemplateCell[][]) => resolveTemplate(t, pal);
  const rf = (t: TemplateCell[][]) => resolveTemplate(flipHorizontal(t), pal);

  const sprites: CharacterSprites = {
    walk: {
      [DIR_DOWN]: [
        r(CHAR_WALK_DOWN_1),
        r(CHAR_WALK_DOWN_2),
        r(CHAR_WALK_DOWN_3),
        r(CHAR_WALK_DOWN_2),
      ],
      [DIR_LEFT]: [
        rf(CHAR_WALK_RIGHT_1),
        rf(CHAR_WALK_RIGHT_2),
        rf(CHAR_WALK_RIGHT_3),
        rf(CHAR_WALK_RIGHT_2),
      ],
      [DIR_RIGHT]: [
        r(CHAR_WALK_RIGHT_1),
        r(CHAR_WALK_RIGHT_2),
        r(CHAR_WALK_RIGHT_3),
        r(CHAR_WALK_RIGHT_2),
      ],
      [DIR_UP]: [
        r(CHAR_WALK_UP_1),
        r(CHAR_WALK_UP_2),
        r(CHAR_WALK_UP_3),
        r(CHAR_WALK_UP_2),
      ],
    },
    typing: {
      [DIR_DOWN]: [r(CHAR_DOWN_TYPE_1), r(CHAR_DOWN_TYPE_2)],
      [DIR_LEFT]: [rf(CHAR_RIGHT_TYPE_1), rf(CHAR_RIGHT_TYPE_2)],
      [DIR_RIGHT]: [r(CHAR_RIGHT_TYPE_1), r(CHAR_RIGHT_TYPE_2)],
      [DIR_UP]: [r(CHAR_UP_TYPE_1), r(CHAR_UP_TYPE_2)],
    },
    reading: {
      [DIR_DOWN]: [r(CHAR_DOWN_READ_1), r(CHAR_DOWN_READ_2)],
      [DIR_LEFT]: [rf(CHAR_RIGHT_READ_1), rf(CHAR_RIGHT_READ_2)],
      [DIR_RIGHT]: [r(CHAR_RIGHT_READ_1), r(CHAR_RIGHT_READ_2)],
      [DIR_UP]: [r(CHAR_UP_READ_1), r(CHAR_UP_READ_2)],
    },
  };

  spriteCache.set(cacheKey, sprites);
  return sprites;
}
