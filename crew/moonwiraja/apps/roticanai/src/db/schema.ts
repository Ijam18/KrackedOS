/**
 * AppLike tracks which users have liked which apps.
 * Used for the social feed feature.
 */
export const appLike = pgTable(
  "app_like",
  {
    id: text("id").primaryKey(), // nanoid
    appId: text("app_id")
      .notNull()
      .references(() => app.id, { onDelete: "cascade" }),
    userId: text("user_id")
      .notNull()
      .references(() => user.id, { onDelete: "cascade" }),
    createdAt: timestamp("created_at").notNull().defaultNow(),
  },
  (table) => [
    uniqueIndex("app_like_app_user_idx").on(table.appId, table.userId),
    index("app_like_user_id_idx").on(table.userId),
  ],
);

