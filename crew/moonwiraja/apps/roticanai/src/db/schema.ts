import {
  boolean,
  index,
  integer,
  jsonb,
  numeric,
  pgTable,
  text,
  timestamp,
  unique,
  uniqueIndex,
} from "drizzle-orm/pg-core";

// =============================================================================
// Better Auth Tables
// =============================================================================

export const user = pgTable(
  "user",
  {
    id: text("id").primaryKey(),
    name: text("name").notNull(),
    email: text("email").notNull().unique(),
    emailVerified: boolean("email_verified").notNull().default(false),
    image: text("image"),
    isGuest: boolean("is_guest").notNull().default(false),
    username: text("username"), // Unique handle for public profile URL
    bio: text("bio"), // Short profile description
    createdAt: timestamp("created_at").notNull().defaultNow(),
    updatedAt: timestamp("updated_at").notNull().defaultNow(),
  },
  (table) => [uniqueIndex("user_username_idx").on(table.username)],
);

export const session = pgTable("session", {
  id: text("id").primaryKey(),
  expiresAt: timestamp("expires_at").notNull(),
  token: text("token").notNull().unique(),
  createdAt: timestamp("created_at").notNull().defaultNow(),
  updatedAt: timestamp("updated_at").notNull().defaultNow(),
  ipAddress: text("ip_address"),
  userAgent: text("user_agent"),
  userId: text("user_id")
    .notNull()
    .references(() => user.id, { onDelete: "cascade" }),
});

export const account = pgTable("account", {
  id: text("id").primaryKey(),
  accountId: text("account_id").notNull(),
  providerId: text("provider_id").notNull(),
  userId: text("user_id")
    .notNull()
    .references(() => user.id, { onDelete: "cascade" }),
  accessToken: text("access_token"),
  refreshToken: text("refresh_token"),
  idToken: text("id_token"),
  accessTokenExpiresAt: timestamp("access_token_expires_at"),
  refreshTokenExpiresAt: timestamp("refresh_token_expires_at"),
  scope: text("scope"),
  password: text("password"),
  createdAt: timestamp("created_at").notNull().defaultNow(),
  updatedAt: timestamp("updated_at").notNull().defaultNow(),
});

export const verification = pgTable("verification", {
  id: text("id").primaryKey(),
  identifier: text("identifier").notNull(),
  value: text("value").notNull(),
  expiresAt: timestamp("expires_at").notNull(),
  createdAt: timestamp("created_at").notNull().defaultNow(),
  updatedAt: timestamp("updated_at").notNull().defaultNow(),
});

// =============================================================================
// App & Message Tables (LLM Generation Persistence)
// =============================================================================

/**
 * App represents a vibe coding project/sandbox.
 * Each app has one conversation thread and one sandbox.
 */
export const app = pgTable(
  "app",
  {
    // nanoid, used in URL: /apps/[id]
    id: text("id").primaryKey(),
    userId: text("user_id")
      .notNull()
      .references(() => user.id, { onDelete: "cascade" }),

    // App metadata
    title: text("title"), // Can be AI-generated from initial prompt
    description: text("description"),
    slug: text("slug").unique(), // Optional custom subdomain for sharing
    status: text("status").notNull().default("active"), // 'active' | 'archived'

    // Modal sandbox reference
    sandboxId: text("sandbox_id"), // Modal sandbox object_id (active session)
    snapshotId: text("snapshot_id"), // Modal snapshot image ID (for restore)
    previewUrl: text("preview_url"), // Current Modal tunnel URL

    // Enhanced version of initial prompt (generated by fast LLM)
    enhancedPrompt: text("enhanced_prompt"),

    // Optional GitHub repo linkage for manual push
    githubRepoId: text("github_repo_id"),
    githubRepoOwner: text("github_repo_owner"),
    githubRepoName: text("github_repo_name"),
    githubRepoUrl: text("github_repo_url"),
    githubDefaultBranch: text("github_default_branch"),
    githubLinkedAt: timestamp("github_linked_at"),
    githubLastPushedAt: timestamp("github_last_pushed_at"),

    // Screenshot thumbnail storage key (e.g., "thumbnails/{appId}.png")
    // Presigned URLs are generated on-demand when displaying
    thumbnailKey: text("thumbnail_key"),

    // Social feed / publishing
    isPublished: boolean("is_published").notNull().default(false),
    publishedAt: timestamp("published_at"), // When the app was published to the feed
    likeCount: integer("like_count").notNull().default(0),
    viewCount: integer("view_count").notNull().default(0),
    remixCount: integer("remix_count").notNull().default(0),
    remixedFromId: text("remixed_from_id"), // If this app was remixed from another

    // Aggregated usage stats (for quick dashboard display)
    totalInputTokens: integer("total_input_tokens").notNull().default(0),
    totalOutputTokens: integer("total_output_tokens").notNull().default(0),
    totalCost: numeric("total_cost", { precision: 10, scale: 6 })
      .notNull()
      .default("0"),

    createdAt: timestamp("created_at").notNull().defaultNow(),
    updatedAt: timestamp("updated_at").notNull().defaultNow(),
  },
  (table) => [
    index("app_user_id_created_at_idx").on(table.userId, table.createdAt),
    index("app_user_id_status_idx").on(table.userId, table.status),
    index("app_slug_idx").on(table.slug),
    index("app_github_repo_id_idx").on(table.githubRepoId),
    index("app_published_at_idx").on(table.isPublished, table.publishedAt),
    index("app_published_likes_idx").on(table.isPublished, table.likeCount),
    index("app_remixed_from_id_idx").on(table.remixedFromId),
  ],
);

/**
 * Message stores individual chat messages with full AI SDK UIMessage compatibility.
 * The `parts` field stores the complete UIMessage.parts array as JSONB.
 */
export const message = pgTable(
  "message",
  {
    // Matches UIMessage.id from AI SDK
    id: text("id").primaryKey(),
    appId: text("app_id")
      .notNull()
      .references(() => app.id, { onDelete: "cascade" }),

    // Message content (UIMessage compatible)
    role: text("role").notNull(), // 'user' | 'assistant' | 'system'
    parts: jsonb("parts").notNull(), // UIMessage.parts array
    metadata: jsonb("metadata"), // UIMessage.metadata (optional)

    // Usage tracking (populated for assistant messages)
    inputTokens: integer("input_tokens"),
    outputTokens: integer("output_tokens"),
    totalTokens: integer("total_tokens"),
    modelId: text("model_id"), // e.g., 'claude-3-5-sonnet-20241022'
    durationMs: integer("duration_ms"), // Response time
    finishReason: text("finish_reason"), // 'stop' | 'length' | 'tool-calls' | etc

    createdAt: timestamp("created_at").notNull().defaultNow(),
  },
  (table) => [
    index("message_app_id_created_at_idx").on(table.appId, table.createdAt),
  ],
);

