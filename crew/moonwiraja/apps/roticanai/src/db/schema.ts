/**
 * Inspo represents a curated example app that users can remix.
 * When remixed, the prompt is used to create a new app.
 */
export const inspo = pgTable(
  "inspo",
  {
    id: text("id").primaryKey(), // nanoid
    title: text("title").notNull(),
    description: text("description").notNull(), // Short display text
    category: text("category").notNull(), // 'games' | 'productivity' | 'utilities' | 'creative'
    prompt: text("prompt").notNull(), // Full prompt used when remixing
    thumbnailUrl: text("thumbnail_url"), // Screenshot URL (external or S3 key)
    featured: boolean("featured").notNull().default(false),
    sortOrder: integer("sort_order").notNull().default(0),
    createdAt: timestamp("created_at").notNull().defaultNow(),
  },
  (table) => [
    index("inspo_category_idx").on(table.category),
    index("inspo_featured_idx").on(table.featured),
  ],
);

