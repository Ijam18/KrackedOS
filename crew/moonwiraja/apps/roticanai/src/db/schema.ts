// Usage Tracking Tables
// =============================================================================

/**
 * DailyUsage tracks the number of messages a user has sent per day.
 * Used for enforcing global daily message limits across all apps.
 * Date is stored in KL timezone (Asia/Kuala_Lumpur).
 */
export const dailyUsage = pgTable(
  "daily_usage",
  {
    id: text("id").primaryKey(), // nanoid
    userId: text("user_id")
      .notNull()
      .references(() => user.id, { onDelete: "cascade" }),
    date: text("date").notNull(), // Format: "2026-01-28" (KL timezone)
    messageCount: integer("message_count").notNull().default(0),
    createdAt: timestamp("created_at").notNull().defaultNow(),
    updatedAt: timestamp("updated_at").notNull().defaultNow(),
  },
  (table) => [
    unique("daily_usage_user_date_unique").on(table.userId, table.date),
    index("daily_usage_user_id_idx").on(table.userId),
  ],
);

/**
 * GuestDailyUsage tracks anonymous browser quota separately from guest user rows.
 * This prevents guest logout from resetting the browser's daily guest limit.
 */
export const guestDailyUsage = pgTable(
  "guest_daily_usage",
  {
    id: text("id").primaryKey(), // nanoid
    quotaKey: text("quota_key").notNull(),
    date: text("date").notNull(), // Format: "2026-01-28" (KL timezone)
    messageCount: integer("message_count").notNull().default(0),
    createdAt: timestamp("created_at").notNull().defaultNow(),
    updatedAt: timestamp("updated_at").notNull().defaultNow(),
  },
  (table) => [
    unique("guest_daily_usage_quota_date_unique").on(
      table.quotaKey,
      table.date,
    ),
    index("guest_daily_usage_quota_key_idx").on(table.quotaKey),
  ],
);

